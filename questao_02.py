""""
2. Escreva um programa que escreva na tela, de 1 até 100,
de 1 em 1, 2 vezes. A primeira vez deve ser usar a estrutura de repetição
for e a segunda while
"""
cont = 1

print('Estrutura de repetição for')
for num in range(1, 101):
    print(num)
print()    
print('Estrutura de repetição while')
while (cont < 101):
    print(cont)
    cont += 1
    
